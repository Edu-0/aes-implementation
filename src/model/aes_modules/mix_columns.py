import numpy as np
import src.model.abstract_algebra as aa


def mix_columns(state):
    return aa.mat_mul(np.array([
    [0x02, 0x03, 0x01, 0x01],
    [0x01, 0x02, 0x03, 0x01],
    [0x01, 0x01, 0x02, 0x03],
    [0x03, 0x01, 0x01, 0x02]],
    dtype=int), state)

def inv_mix_columns(state):
    return aa.mat_mul(np.array([
    [0x0E, 0x0B, 0x0D, 0x09],
    [0x09, 0x0E, 0x0B, 0x0D],
    [0x0D, 0x09, 0x0E, 0x0B],
    [0x0B, 0x0D, 0x09, 0x0E]],
    dtype=int), state)